package homework1;

public class TableLamp {
    private Double lampshadeSize;
    private String color;
    private Double hight;
    private int lampPower;
    private String powerUnit;

    // ----------------------G/S----------------------------


    public Double getLampshadeSize() {
        return lampshadeSize;
    }

    public void setLampshadeSize(Double lampshadeSize) {
        this.lampshadeSize = lampshadeSize;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public Double getHight() {
        return hight;
    }

    public void setHight(Double hight) {
        this.hight = hight;
    }

    public int getLampPower() {
        return lampPower;
    }

    public void setLampPower(int lampPower) {
        this.lampPower = lampPower;
    }

    public String getPowerUnit() {
        return powerUnit;
    }

    public void setPowerUnit(String powerUnit) {
        this.powerUnit = powerUnit;
    }
}
